    private void txtKolicinaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKolicinaKeyReleased

        try {
            if ((evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9')
                    || evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {

                if (txtKolicina.getText().isEmpty()) {
                    txtCenaStavke.setText("0.00");
                    return;
                }

                Sladoled s = (Sladoled) cmbSladoled.getSelectedItem();
                double kolicina = Double.parseDouble(txtKolicina.getText());

                txtCenaStavke.setText(String.valueOf(s.getCenaPoKG() * kolicina));

            }
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Morate uneti broj u kolicinu!");
        }


    }//GEN-LAST:event_txtKolicinaKeyReleased

    private void cmbSladoledItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbSladoledItemStateChanged

        if (cmbSladoled.getSelectedItem() != null) {

            if (txtKolicina.getText().isEmpty()) {
                txtCenaStavke.setText("0.00");
                return;
            }

            Sladoled s = (Sladoled) cmbSladoled.getSelectedItem();
            double kolicina = Double.parseDouble(txtKolicina.getText());

            txtCenaStavke.setText(String.valueOf(s.getCenaPoKG() * kolicina));

        }

    }//GEN-LAST:event_cmbSladoledItemStateChanged

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (txtKolicina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti kolicinu stavke!");
            return;
        }

        Sladoled c = (Sladoled) cmbSladoled.getSelectedItem();
        double kolicina = Double.parseDouble(txtKolicina.getText());
        double cenaStavke = Double.parseDouble(txtCenaStavke.getText());

        StavkaRacuna sr = new StavkaRacuna(null, -1, kolicina, cenaStavke, c);

        TableModelStavkeRacuna tm = (TableModelStavkeRacuna) tblStavke.getModel();
        tm.dodajStavku(sr);

        double ukupanIznos = Double.parseDouble(txtUkupanIznos.getText());

        ukupanIznos += sr.getCenaStavke();

        txtUkupanIznos.setText(String.valueOf(ukupanIznos));


    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int row = tblStavke.getSelectedRow();

        if (row >= 0) {
            TableModelStavkeRacuna tm = (TableModelStavkeRacuna) tblStavke.getModel();
            
            StavkaRacuna sr = tm.getStavka(row);

            double ukupanIznos = Double.parseDouble(txtUkupanIznos.getText());

            ukupanIznos -= sr.getCenaStavke();

            txtUkupanIznos.setText(String.valueOf(ukupanIznos));

            tm.obrisiStavku(row);
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        
        Kupac k = (Kupac) cmbKupac.getSelectedItem();
        double ukupanIznos = Double.parseDouble(txtUkupanIznos.getText());
        
        TableModelStavkeRacuna tm = (TableModelStavkeRacuna) tblStavke.getModel();
        
        Racun r = new Racun(null, new Date(), ukupanIznos, k, a, tm.getLista());
        
        try {
            ClientController.getInstance().addRacun(r);
            JOptionPane.showMessageDialog(this, "Sistem je kreirao raƒçun!");
            srediFormu();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

