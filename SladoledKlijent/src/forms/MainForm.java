    @Override
    public void run() {
        while (true) {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");
            lblDatumVreme.setText("Datum i vreme: " + sdf.format(new Date()));
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void srediFormu() {
        popuniKupce();
        popuniSladolede();
        txtUkupanIznos.setText("0.00");
        txtUkupanIznos.setEnabled(false);
        txtCenaStavke.setText("0.00");
        txtCenaStavke.setEnabled(false);
        tblStavke.setModel(new TableModelStavkeRacuna());
    }

    public void popuniKupce() {
        try {
            ArrayList<Kupac> kupci = ClientController.getInstance().getAllKupac();

            cmbKupac.removeAllItems();

            for (Kupac kupac : kupci) {
                cmbKupac.addItem(kupac);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

  

    private void popuniSladolede() {
        try {
            ArrayList<Sladoled> sladoledi = ClientController.getInstance().getAllSladoled();

            cmbSladoled.removeAllItems();

            for (Sladoled sladoled : sladoledi) {
                cmbSladoled.addItem(sladoled);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormNoviSladoled.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

